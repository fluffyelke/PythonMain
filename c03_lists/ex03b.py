places = ['japan', 'england', 'germany', 'italy', 'north korea']
print("Original List:")
print(places)

print("\nSorted List:")
print(sorted(places))

print("\nOriginal List:")
print(places)

print("\nSorted in Reverse List:")
print(sorted(places, reverse = True))

print("Original List:")
print(places)

places.reverse()
print("\nReversed List:")
print(places)

places.reverse()
print("\nReturn to normal using Reversed again:")
print(places)

places.sort()
print("\nSort the list alphabeticaly:")
print(places)

places.sort(reverse = True)
print("\nReverse the list using sort:")
print(places)
